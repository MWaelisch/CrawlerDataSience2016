package model;

public class PlebFriend {
	private long id;
	private long friend;
	
	public PlebFriend(){}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public long getFriend() {
		return friend;
	}

	public void setFriend(long friend) {
		this.friend = friend;
	}
}
